#test to help to identify a suitable profession. This cod can be used as a survey or application tool


def conduct_career_test():
    questions = [
        {"question": "Вам нравится решать логические задачи?", "category": "Технические профессии"},
        {"question": "Вам интересно работать с людьми?", "category": "Социальные профессии"},
        {"question": "Вы любите создавать что-то новое (писать, рисовать, сочинять)?",
         "category": "Креативные профессии"},
        {"question": "Вам нравится управлять проектами и координировать работу других?", "category": "Менеджмент"},
        {"question": "Вам интересно изучать технологии?", "category": "Технические профессии"},
        {"question": "Вы бы хотели помогать людям в сложных ситуациях?", "category": "Социальные профессии"},
        {"question": "Вы предпочитаете работать над личными проектами, а не командными?",
         "category": "Креативные профессии"},
        {"question": "Вам нравится планировать и организовывать задачи?", "category": "Менеджмент"},
        {"question": "Вы любите работать с программами и вычислениями?", "category": "Технические профессии"},
        {"question": "Вы любите обучать и консультировать людей?", "category": "Социальные профессии"},
        {"question": "Творчество для вас важно в работе?", "category": "Креативные профессии"},
        {"question": "Вы чувствуете себя уверенно, принимая важные решения?", "category": "Менеджмент"},
        {"question": "Вы заинтересованы в разработке новых продуктов?", "category": "Технические профессии"},
        {"question": "Вы чувствуете удовлетворение от помощи другим?", "category": "Социальные профессии"},
        {"question": "Вы любите находить необычные подходы к задачам?", "category": "Креативные профессии"},
        {"question": "Вы уверенно берёте на себя лидерство?", "category": "Менеджмент"},
        {"question": "Вам интересно разрабатывать алгоритмы и коды?", "category": "Технические профессии"},
        {"question": "Вам комфортно общаться с новыми людьми?", "category": "Социальные профессии"},
        {"question": "Вы вдохновляетесь искусством и дизайном?", "category": "Креативные профессии"},
        {"question": "Вам нравится контролировать выполнение задач в команде?", "category": "Менеджмент"},
    ]

    scores = {
        "Социальные профессии": 0,
        "Технические профессии": 0,
        "Креативные профессии": 0,
        "Менеджмент": 0,
    }

    for item in questions:
        question = item["question"]
        category = item["category"]

        while True:
            answer = input(question + " (да/нет) ").strip().lower()
            if answer == "да":
                scores[category] += 1
                break
            elif answer == "нет":
                break
            else:
                print("Пожалуйста, введите 'да' или 'нет'.")

    professions = {
        "Социальные профессии": ["Психолог", "Социальный работник", "Учитель"],
        "Технические профессии": ["Программист", "Инженер", "Системный администратор"],
        "Креативные профессии": ["Дизайнер", "Художник", "Копирайтер"],
        "Менеджмент": ["Менеджер проектов", "Руководитель команды", "Операционный менеджер"],
    }

    recommended_categories = [category for category, score in scores.items() if score > 2]

    if recommended_categories:
        print("На основе ваших ответов вам может подойти следующее направление:")
        for category in recommended_categories:
            print(f"- {category}")

            print("  Рекомендуемые профессии:")
            for profession in professions[category]:
                print(f"  - {profession}")
    else:
        print("К сожалению, подходящих профессий не найдено.")



conduct_career_test()
